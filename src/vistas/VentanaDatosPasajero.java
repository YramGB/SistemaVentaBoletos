    public boolean cargarDescuentos(JComboBox comboBox){
                     
        Conexion conn = new Conexion();
        Connection reg = conn.conectar();  
        Connection con = reg; 
         
        PreparedStatement ps = null;
        ResultSet rs = null;                       
        
        String sql = "SELECT tipoDescuento FROM descuento";
        
        try{
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            comboBox.addItem("Seleccione un tipo de boleto");
            
            while(rs.next()){
                comboBox.addItem(rs.getString("tipoDescuento"));
            }
            return true;
        }catch(Exception e){
            System.out.println("Error en la consulta: " + e);
            return false;
        }      
    }  
    
