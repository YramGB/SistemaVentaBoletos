    private void continuarBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continuarBtn1ActionPerformed
                       
        Boleto boleto = new Boleto();
        Pasajero pasajero = new Pasajero();
        Descuento descuento = new Descuento();
        Autobus autobus = new Autobus();
        Asiento asiento = new Asiento();
        
        if(nombreTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar el nombre del pasajero", "Datos incompletos", JOptionPane.WARNING_MESSAGE);
        }else if(apellidoMaternoTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar el apellido paterno del pasajero", "Datos incompletos", JOptionPane.WARNING_MESSAGE);
        }else if(apellidoPaternoTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar el apellido materno del pasajero", "Datos incompletos", JOptionPane.WARNING_MESSAGE);
        }else if(descuentoCbox.getSelectedItem().equals("Seleccione un tipo de boleto")){
            JOptionPane.showMessageDialog(this, "Debe elegir un descuento", "Datos incompletos", JOptionPane.WARNING_MESSAGE);            
        }else{
                        
            //origen        
            autobus.setNombreOrigen(setOrigenLabel.getText());  
            //destino        
            autobus.setNombreDestino(setDestinoLabel.getText());
            //Precio viaje
            double precio = Double.parseDouble(setPrecioLabel.getText());
            autobus.setPrecioViaje(precio);
            //fecha
            autobus.setFechaViaje(setFechaLabel.getText());
            //horario        
            autobus.setHorarioViaje(setHorarioLabel.getText());  
            //Asiento
            int asientoInt = Integer.parseInt(setAsientoLabel.getText());
            asiento.setNumeroAsiento(asientoInt);                                              
            asiento.add(asiento);            
            
            int numeroAutobus = Integer.parseInt(numeroAutobusTxt.getText());
            autobus.setNumeroAutobus(numeroAutobus);  
            String tipoDescuento = descuentoCbox.getSelectedItem().toString();
            descuento.setTipoDescuento(tipoDescuento);
            boleto.seleccionarDescuento(descuento);
            pasajero.setNombrePasajero(nombreTxt.getText());
            pasajero.setApellidoPaternoPasajero(apellidoPaternoTxt.getText());
            pasajero.setApellidoMaternoPasajero(apellidoMaternoTxt.getText());                                      
            
            pasajero.add(pasajero);
                       
            Double precioAnterior = Double.parseDouble(setPrecioAnterior.getText());
            boleto.setPrecioFinal(precioAnterior);
            
            if(precioAnterior != 0.0){
                VentanaNuevoCobro nuevoCobro = new VentanaNuevoCobro(pasajero, boleto, autobus, descuento, asiento);
                nuevoCobro.setVisible(true);
                this.setVisible(false);
            }else{
                VentanaCobrar cobrar = new VentanaCobrar(pasajero, boleto, autobus, descuento, asiento);
                cobrar.setVisible(true);
                this.setVisible(false); 
            }
            
        }                
    }//GEN-LAST:event_continuarBtn1ActionPerformed

