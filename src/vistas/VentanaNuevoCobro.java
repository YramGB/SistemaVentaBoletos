    private void cobrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cobrarBtnActionPerformed
        Autobus autobus = new Autobus();
        Pasajero pasajero = new Pasajero();
        Descuento descuento = new Descuento();
        Boleto boleto = new Boleto();
        Asiento asiento = new Asiento();
        Asiento arrayAsiento = new Asiento();

        if(pagoTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar el monto dado por el pasajero", "Monto no ingresado", JOptionPane.WARNING_MESSAGE);
        }else{
            //Numero autobus
            int numeroAutobus = Integer.parseInt(numeroAutobusTxt.getText());
            autobus.setNumeroAutobus(numeroAutobus);
            //origen
            autobus.setNombreOrigen(setOrigenLabel.getText());
            //destino
            autobus.setNombreDestino(setDestinoLabel.getText());
            //fecha
            autobus.setFechaViaje(setFechaLabel.getText());
            //horario
            autobus.setHorarioViaje(setHorarioLabel.getText());
            //nombre
            pasajero.setNombrePasajero(setNombre.getText());
            //apellidoMaterno
            pasajero.setApellidoPaternoPasajero(setApellidoPaterno.getText());
            //apellidoPaterno
            pasajero.setApellidoMaternoPasajero(setApellidoMaterno.getText());
            //Asiento
            int asientoInt = Integer.parseInt(setAsientoLabel.getText());
            boleto.setNumeroAsiento(asientoInt);
            //descuento
            descuento.setTipoDescuento(setDescuentoTxt.getText());
            //Total Final
            double precio = Double.parseDouble(setTotalPagar.getText());
            boleto.setPrecioFinal(precio);

            arrayAsiento.setNumeroAsiento(asientoInt);
            arrayAsiento.add(arrayAsiento);

            if(boleto.generarBoleto(pasajero, boleto, autobus, descuento)){
                asiento.setNumeroAutobus(numeroAutobus);
                asiento.setNumeroAsiento(asientoInt);
                asiento.setEstado(0);
                asiento.cambiarEstado(asiento);
                VentanaDatosBoleto boletoVendido = new VentanaDatosBoleto(pasajero, boleto, autobus, descuento, asiento);
                boletoVendido.setVisible(true);
                this.setVisible(false);

            }else{
                System.out.println("FallÃ³ algo :(");
            }
        }
    }//GEN-LAST:event_cobrarBtnActionPerformed

    private void pagoTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pagoTxtKeyReleased
        try{
            double pago = Double.parseDouble(pagoTxt.getText());
            double totalPagar = Double.parseDouble(setTotalPagar.getText());
            double cambio = 0.0;
            cambio = pago - totalPagar;
            String cambioS = String.valueOf(cambio);
            setCambio.setText(cambioS);
        }catch(Exception e){
            System.out.println("Error: " + e);
        }
    }//GEN-LAST:event_pagoTxtKeyReleased

